export const getThemeVariables = (themeId, mode) => {
  if (themeId === 'custom') {
    // Return custom theme from localStorage
    const customTheme = localStorage.getItem('customTheme');
    if (customTheme) {
      try {
        const parsed = JSON.parse(customTheme);
        return parsed[mode] || themes['modern-teal'][mode];
      } catch (e) {
        console.error('Failed to parse custom theme:', e);
        return themes['modern-teal'][mode];
      }
    }
    return themes['modern-teal'][mode];
  }
  
  return themes[themeId]?.[mode] || themes['modern-teal'][mode];
};

export const applyTheme = (themeId, mode) => {
  const variables = getThemeVariables(themeId, mode);
  const root = document.documentElement;
  
  Object.entries(variables).forEach(([key, value]) => {
    root.style.setProperty(`--${key}`, value);
  });
  
  root.setAttribute('data-theme', mode);
};

